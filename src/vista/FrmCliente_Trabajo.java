    public void Refresh(){
        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Telefono");
        modelo.addColumn("Direccion");
        modelo.addColumn("RUC");
        clientesLista=clientesControl.listarClientes("");
        Object[] fila = new Object[6];
        for (int i = 0; i < clientesLista.size(); i++) {
            clientesObj = clientesLista.get(i);
            fila[0] = clientesObj.getCli_codigo();
            fila[1] = clientesObj.getCli_nombre();
            fila[2] = clientesObj.getCli_apellido();
            fila[3] = clientesObj.getCli_telefono();
            fila[4] = clientesObj.getCli_direccion();
            fila[5] = clientesObj.getCli_ruc();
            modelo.addRow(fila);
        }
        tblClientes.setModel(modelo);
        
    }
